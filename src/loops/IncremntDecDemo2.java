package loops;

public class IncremntDecDemo2 {

    public static void main(String[] args) {

        int i = 10;
        System.out.println(i); // 10
        System.out.println(i--); // 10
        System.out.println(i); // 9
        System.out.println(--i); // 8
        System.out.println(i); // 8

    //i++ / i - -  : first statements will execute
        // then increment or decrement will happen

   // ++i / - -i  : first increment or decrement will happen
        // then statements will execute




    }
}
