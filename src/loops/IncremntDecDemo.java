package loops;

public class IncremntDecDemo {

    public static void main(String[] args) {

        int i = 10;

        System.out.println(i); // 10
        System.out.println(i++); // 10
        System.out.println(i); // 11
        System.out.println(++i); //12
        System.out.println(i); // 12

    //i++ / i - -  : first statements will execute then increment or decrement will happen
   // ++i / - -i  : first increment or decrement will happen then statements will execute




    }
}
